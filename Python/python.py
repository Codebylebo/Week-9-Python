#Operators

# Addition (+)
# Subtraction (-)
# Multiplication (*)
# Division (/)
# Modulus (%)
# Exponentiation (**)
# Floor Division (//)

x = 10
y = 2

print(x + y)  # Addition: Output: 12
print(x - y)  # Subtraction: Output: 8
print(x * y)  # Multiplication: Output: 20
print(x / y)  # Division: Output: 5.0
print(x % y)  # Modulus: Output: 0 # Remainder of division
print(x ** y)  # Exponentiation: Output: 100
print(x // y)  # Floor Division: Output: 5 # Rounds down to the nearest integer

# Assignment Operators
x = 10
x = x + 2 # Equivalent to x += 2
print(x)  # Output: 12

x = 10
x -= 2  # Equivalent to x = x - 2
print(x)  # Output: 8