# Modules
# Module is a file containing Python code. It can define functions, classes, and variables.
# Modules allow you to organize your code into separate files, making it easier to manage and reuse

import math_operations # Importing the math_operations module

print(math_operations.add(5, 2))  # Output: 7

print(math_operations.subtract(5, 2))  # Output: 3

# modules are reusable pieces of code that can be imported into other Python scripts.
# You can create your own modules by saving Python code in a file with a .py extension